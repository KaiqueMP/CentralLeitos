    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        numTentativas ++;
        
        Usuario usuario;
        
        try {
            usuario = (Usuario) em.createQuery("SELECT u FROM Usuario u WHERE u.login = :login")
                                  .setParameter("login", txtLogin.getText())
                                  .getSingleResult();
        } catch(NoResultException e) {
            JOptionPane.showMessageDialog(this,"Login invalido");
            usuario = null;
        }
        
        String password = String.valueOf(senha.getPassword());
        String codificado = Base64.getEncoder().encodeToString(password.getBytes());
        
        
        if(usuario != null && usuario.getSenha().equals(codificado)){
            this.setVisible(false);
            autenticado = true;
            JOptionPane.showMessageDialog(this, "Logado com sucesso!");
        }
            
        else if(numTentativas >= 3) {
            JOptionPane.showMessageDialog(this, "Máximo de tentativas atingidas, o sistema será finalizado!");
            autenticado = false;
        } 
        else {
            JOptionPane.showMessageDialog(this, "Usuário ou Senha incorretos, tente novamente!");
            txtLogin.requestFocus(); 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

