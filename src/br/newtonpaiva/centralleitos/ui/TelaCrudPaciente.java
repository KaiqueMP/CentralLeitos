    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        em.getTransaction().begin();
        Paciente p;
        
        String message = "";
        if(this.idPaciente == null){
            p = new Paciente();     
            message = "cadastrado";
            em.persist(p);  
        } else {
            p = em.find(Paciente.class, idPaciente);
            message = "editado";
            em.merge(p);
        }
        
        p.setNome(txtName.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
	Calendar calN = Calendar.getInstance();//nascimento
        Calendar calA = Calendar.getInstance();//admissao
        try {
            calN.setTime(sdf.parse(txtDtaNascimento.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(TelaCrudPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
        p.setDataNascimento(calN);
        if(cbxSexo.getSelectedIndex()== 0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para o sexo do paciente.");
            return;
        } else {
            p.setSexo(cbxSexo.getSelectedItem().toString());
        }
        if(cbxTipoSan.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para o tipo sanguíneo do paciente.");
            return;
        } else {
            p.setTipoSanguineo(cbxTipoSan.getSelectedItem().toString());
        }
        p.setNomeResp(txtNameResp.getText());
        p.setGrauParentesco(txtParentesco.getText());
        p.setTelefone(txtTelefone.getText());
        p.setDadosEnfermaria(txtEnfermaria.getText());
        p.setClinica(txtClinica.getText());
        p.setTelefoneClinica(txtTelClinica.getText());
        //l.setId();
        p.setNomeMedico(txtMedico.getText());
        p.setNomeEnfermeiro(txtEnfermeiro.getText());
        p.setCoren(txtCoren.getText());
        
        try {
            calA.setTime(sdf.parse(txtDtaAdimissao.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(TelaCrudPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
        p.setDataAdimissao(calA);
        
        if(cbxOncologico.getSelectedIndex()== 0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para o paciente oncológico paliatico.");
            return;
        } else {
            p.setPacienteOncologico(cbxOncologico.getSelectedItem().toString());
        }
        p.setMotivoInternacao(txtMotivoInternacao.getText());
        p.setDiagnosticoPrincipal(txtDiagnostico.getText());
        p.setCid(txtCid.getText());
        p.setDiagnosticoSecundario(txtDiagnosticoSec.getText());
        p.setIntercorrencias(txtIntercorrencias.getText());
        if(cbxAlergia.getSelectedIndex()== 0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para alergia.");
            return;
        } else {
            p.setAlergia(cbxAlergia.getSelectedItem().toString());
        }
        p.setDescAlergia(txtDescAlergia.getText());
        if(cbxFebre.getSelectedIndex()== 0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para febre.");
            return;
        } else {
            p.setFebre(cbxFebre.getSelectedItem().toString());
        }
        if(cbxInternadoUti.getSelectedIndex()== 0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para Internado em UTI.");
            return;
        } else {
            p.setInternadoUti(cbxInternadoUti.getSelectedItem().toString());
        }
        if(cbxIsolamento.getSelectedIndex()== 0){
            JOptionPane.showMessageDialog(this, "Selecione uma opção válida para esteve em isolamento.");
            return;
        } else {
            p.setIsolamento(cbxIsolamento.getSelectedItem().toString());
        }
        p.setPatogeno(txtPatogeno.getText());
        p.setAltura(Double.parseDouble(txtAltura.getText()));
        p.setPeso(Double.parseDouble(txtPeso.getText()));
        p.setCrm(txtCrm.getText());
        if (txtCpf.getText().equals("   .   .   -  ")){
            JOptionPane.showMessageDialog(null, "O CPF é obrigatório","AVISO!",JOptionPane.WARNING_MESSAGE);
            txtCpf.requestFocus();
             return;
        }else
            p.setCpf(txtCpf.getText());
        
        if(txtEndereco.getText().trim().isEmpty())
            p.setEndereco(null);
        else{
            p.setEndereco(txtEndereco.getText());
        }
        if(txtCep.getText().trim().isEmpty())
            p.setCep(null);
        else{
            p.setCep(txtCep.getText());
        }
        if(txtBairro.getText().trim().isEmpty())
            p.setBairro(null);
        else{
            p.setBairro(txtBairro.getText());
        }        
        if(cbxUF.getSelectedIndex()==0)
            p.setUF(null);
        else{
            p.setUF(Integer.toString(cbxUF.getSelectedIndex()));
        }
        if(txtNumCasa.getText().trim().isEmpty())
            p.setNumero(null);
        else{
            p.setNumero(Integer.parseInt(txtNumCasa.getText()));
        }
        if(txtCidade.getText().trim().isEmpty()){
            p.setCidade(null);
        }else{
            p.setCidade(txtCidade.getText());
        }
        em.persist(p);
        em.getTransaction().commit();
        em.close();
        
        JOptionPane.showMessageDialog(this, "Paciente "+ message +" com sucesso!");
        btnLimpar.doClick();
    }//GEN-LAST:event_btnSalvarActionPerformed

