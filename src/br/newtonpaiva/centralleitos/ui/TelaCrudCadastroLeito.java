    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        em.getTransaction().begin();
        
        Leito l;
        if(this.Leito == 0) {
            l = new Leito();
            em.persist(l);
            JOptionPane.showMessageDialog(this, "Leito cadastrada com Sucesso!");
        }else{
            l = em.find(Leito.class, Leito);
            em.merge(l);
            JOptionPane.showMessageDialog(this, "Leito alterada com Sucesso!!");
        }
        
        
        
        l.setTipoLeito((String) cbxLeito.getSelectedItem());
        l.setAla((String) cbxAla.getSelectedItem());
        l.getChave();

        em.getTransaction().commit();
        
        //btnLimpar.doClick(); professor falou pra n�o usar mais o bot�o limpar, ultrapassado
        //btnBuscar.doClick();
    }//GEN-LAST:event_btnSalvarActionPerformed

