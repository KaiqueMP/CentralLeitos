    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        List<Leito> leito = em.createQuery("SELECT l FROM Leito l").getResultList();
        
        DefaultTableModel model = (DefaultTableModel) tblLeitos.getModel();
        
        model.setRowCount(0);
        for(Leito l: leito) {
           model.addRow(new Object[] { 
               l.getTipoLeito(), 
               l.getAla(),
               l.getChave()
           }); 
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int row = tblLeitos.getSelectedRow();
        
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "Favor selecionar um Leito!");
        }else{
            Integer id = (Integer) tblLeitos.getModel().getValueAt(row, 0);
            Leito l = em.find(Leito.class, id);
            
            em.getTransaction().begin();
            em.remove(l);
            em.getTransaction().commit();
            
            btnBuscar.doClick();
            JOptionPane.showMessageDialog(this, "Leito excluido com sucesso!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

