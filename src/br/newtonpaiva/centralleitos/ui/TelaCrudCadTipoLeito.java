    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        List<TipoLeito> TipoLeito = em.createQuery("SELECT tp FROM TipoLeito tp").getResultList();
        
        DefaultTableModel model = (DefaultTableModel) tbTipoLeito.getModel();
        
        model.setRowCount(0);
        for(TipoLeito tp: TipoLeito) {
           model.addRow(new Object[] { 
               tp.getDescricao(),
               tp.getChave()
           }); 
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int row = tbTipoLeito.getSelectedRow();
        
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "Favor selecionar um Leito!");
        }else{
            Integer id = (Integer) tbTipoLeito.getModel().getValueAt(row, 0);
            TipoLeito tp = em.find(TipoLeito.class, id);
            
            em.getTransaction().begin();
            em.remove(tp);
            em.getTransaction().commit();
            
            btnBuscar.doClick();
            JOptionPane.showMessageDialog(this, "Leito excluido com sucesso!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

