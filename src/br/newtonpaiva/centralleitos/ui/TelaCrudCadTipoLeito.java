    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        em.getTransaction().begin();
        String texto = txtTipoLeito.getText();
        
        TipoLeito tp;
        if(this.TipoLeito == null) {
            tp = new TipoLeito();
            em.persist(tp);
            JOptionPane.showMessageDialog(this, "Tipo de Leito cadastrado com Sucesso!");
        }else{
            tp = em.find(TipoLeito.class, TipoLeito);
            em.merge(tp);
            JOptionPane.showMessageDialog(this, "Tipo de Leito alterado com Sucesso!!");
        }
        
        tp.setDescricao(txtTipoLeito.getText());
        tp.setChave(texto.substring(0, 3));

        em.getTransaction().commit();
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

